def example_json_load():
    """
    Demonstrate reading a JSON file using json.load.
    """
    print("=" * 60)
    print("  Example 5: Reading JSON with json.load")
    print("=" * 60)

    with open(JSON_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Display structure info
    if isinstance(data, dict) and "employees" in data:
        employees = data["employees"]
        print(f"  Loaded {len(employees)} employees from JSON\n")

        for emp in employees:
            print(f"  ID: {emp['id']} | {emp['name']:<20} | "
                  f"Dept: {emp['department']:<15} | Salary: ${emp['salary']:,}")
    elif isinstance(data, list):
        print(f"  Loaded {len(data)} records from JSON\n")
        for item in data[:5]:  # Show first 5
            print(f"  {item}")

    print()


def example_json_loads():
    """
    Demonstrate parsing a JSON string using json.loads.
    """
    print("=" * 60)
    print("  Example 6: Parsing JSON string with json.loads")
    print("=" * 60)

    # Simulating JSON data received from an API
    api_response = '''
    {
        "status": "success",
        "data": {
            "user": "alice",
            "role": "admin",
            "permissions": ["read", "write", "delete"],
            "active": true
        },
        "timestamp": "2025-05-15T10:30:00Z"
    }
    '''

    # Parse the JSON string
    result = json.loads(api_response)

    print(f"  Status: {result['status']}")
    print(f"  User:   {result['data']['user']}")
    print(f"  Role:   {result['data']['role']}")
    print(f"  Perms:  {', '.join(result['data']['permissions'])}")
    print(f"  Active: {result['data']['active']}")
    print(f"  Time:   {result['timestamp']}")
    print()


