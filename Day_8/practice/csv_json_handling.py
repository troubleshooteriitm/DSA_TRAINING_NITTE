import csv
import json
import os
from pathlib import Path

# Get the directory where this script is located
SCRIPT_DIR = Path(__file__).parent

# File paths for sample data
CSV_FILE = SCRIPT_DIR / "sample_data.csv"
JSON_FILE = SCRIPT_DIR / "sample_data.json"

# Output file paths (generated by this script)
CSV_OUTPUT = SCRIPT_DIR / "output_employees.csv"
JSON_OUTPUT = SCRIPT_DIR / "output_employees.json"
CONVERTED_CSV = SCRIPT_DIR / "converted_from_json.csv"
CONVERTED_JSON = SCRIPT_DIR / "converted_from_csv.json"


# ============================================================
# 1. READING CSV FILES
# ============================================================

def example_csv_reader():
    """
    Demonstrate reading a CSV file using csv.reader.
    Each row is returned as a list of strings.
    """
    print("=" * 60)
    print("  Example 1: Reading CSV with csv.reader")
    print("=" * 60)

    with open(CSV_FILE, "r", newline="", encoding="utf-8") as f:
        reader = csv.reader(f)

        # Read the header row
        header = next(reader)
        print(f"  Columns: {header}")
        print()

        # Read and display each data row
        for row_num, row in enumerate(reader, start=1):
            print(f"  Row {row_num}: {row}")

    print()


def example_csv_dictreader():
    """
    Demonstrate reading a CSV file using csv.DictReader.
    Each row is returned as a dictionary with column names as keys.
    """
    print("=" * 60)
    print("  Example 2: Reading CSV with csv.DictReader")
    print("=" * 60)

    with open(CSV_FILE, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)

        print(f"  Fieldnames: {reader.fieldnames}")
        print()

        # Access each field by name -- much more readable!
        for row in reader:
            name = row["name"]
            dept = row["department"]
            salary = row["salary"]
            print(f"  {name:<20} | Dept: {dept:<15} | Salary: ${salary}")

    print()


