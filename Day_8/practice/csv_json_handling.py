def example_json_dumps():
    """
    Demonstrate converting Python objects to JSON strings using json.dumps.
    Shows pretty printing capabilities.
    """
    print("=" * 60)
    print("  Example 8: Pretty Printing JSON with json.dumps")
    print("=" * 60)

    data = {
        "students": [
            {"name": "Alice", "grade": "A", "gpa": 3.9},
            {"name": "Bob", "grade": "B+", "gpa": 3.4},
            {"name": "Charlie", "grade": "A-", "gpa": 3.7},
        ],
        "class": "Computer Science 101",
        "semester": "Fall 2025"
    }

    # Compact format (single line)
    compact = json.dumps(data, separators=(",", ":"))
    print(f"  Compact ({len(compact)} chars):")
    print(f"  {compact[:80]}...")

    # Pretty format
    pretty = json.dumps(data, indent=2, sort_keys=True)
    print(f"\n  Pretty ({len(pretty)} chars):")
    print(pretty)
    print()


