def example_csv_to_json():
    """
    Convert CSV data to JSON format.
    """
    print("=" * 60)
    print("  Example 9: Converting CSV  JSON")
    print("=" * 60)

    # Read CSV data
    employees = []
    with open(CSV_FILE, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric fields from strings
            row["id"] = int(row["id"])
            row["salary"] = int(row["salary"])
            employees.append(row)

    # Write as JSON
    output = {"employees": employees, "count": len(employees)}
    with open(CONVERTED_JSON, "w", encoding="utf-8") as f:
        json.dump(output, f, indent=4)

    print(f"   Converted {len(employees)} records from CSV to JSON")
    print(f"  Output: {CONVERTED_JSON.name}")

    # Show preview
    with open(CONVERTED_JSON, "r", encoding="utf-8") as f:
        content = f.read()
    # Show first 500 characters
    preview = content[:500] + ("..." if len(content) > 500 else "")
    print(f"\n  Preview:\n{preview}")
    print()


def example_json_to_csv():
    """
    Convert JSON data to CSV format.
    """
    print("=" * 60)
    print("  Example 10: Converting JSON  CSV")
    print("=" * 60)

    # Read JSON data
    with open(JSON_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Extract employees list
    employees = data.get("employees", data) if isinstance(data, dict) else data

    if not employees:
        print("    No employee data found in JSON file.")
        return

    # Write as CSV
    fieldnames = list(employees[0].keys())

    with open(CONVERTED_CSV, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(employees)

    print(f"   Converted {len(employees)} records from JSON to CSV")
    print(f"  Output: {CONVERTED_CSV.name}")

    # Show the CSV content
    with open(CONVERTED_CSV, "r", encoding="utf-8") as f:
        content = f.read()
    print(f"\n  CSV content:\n{content}")


