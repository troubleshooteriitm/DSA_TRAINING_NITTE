def example_csv_writer():
    """
    Demonstrate writing a CSV file using csv.writer.
    """
    print("=" * 60)
    print("  Example 3: Writing CSV with csv.writer")
    print("=" * 60)

    # Sample data to write
    header = ["product", "category", "price", "quantity"]
    products = [
        ["Laptop", "Electronics", 999.99, 50],
        ["Desk Chair", "Furniture", 249.99, 120],
        ["Python Book", "Books", 39.99, 300],
        ["Monitor", "Electronics", 449.99, 75],
        ["Notebook", "Stationery", 4.99, 1000],
    ]

    with open(CSV_OUTPUT, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(header)      # Write header
        writer.writerows(products)   # Write all data rows at once

    print(f"   Written {len(products)} rows to: {CSV_OUTPUT.name}")

    # Verify by reading back
    with open(CSV_OUTPUT, "r", newline="", encoding="utf-8") as f:
        content = f.read()
    print(f"\n  File contents:\n{content}")


def example_csv_dictwriter():
    """
    Demonstrate writing a CSV file using csv.DictWriter.
    """
    print("=" * 60)
    print("  Example 4: Writing CSV with csv.DictWriter")
    print("=" * 60)

    fieldnames = ["city", "country", "population", "timezone"]
    cities = [
        {"city": "Tokyo", "country": "Japan", "population": 13960000, "timezone": "JST"},
        {"city": "Mumbai", "country": "India", "population": 12440000, "timezone": "IST"},
        {"city": "São Paulo", "country": "Brazil", "population": 12330000, "timezone": "BRT"},
        {"city": "Berlin", "country": "Germany", "population": 3645000, "timezone": "CET"},
    ]

    output_path = SCRIPT_DIR / "cities.csv"
    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(cities)

    print(f"   Written {len(cities)} cities to: {output_path.name}")

    # Read back and display
    with open(output_path, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            pop = int(row["population"])
            print(f"  {row['city']:<12} | {row['country']:<10} | "
                  f"Pop: {pop:>12,} | TZ: {row['timezone']}")
    print()


